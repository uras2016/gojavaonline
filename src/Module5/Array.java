package Module5;


public class Array {
    public static void main(String[] args) {
        double[] doubleArray = {1.2, 2.2, 3.5, -10.0, 5.0};


        for (int i = 0; i < doubleArray.length; i++) { // количество элементов в масивеж
            System.out.println(doubleArray[i]);// в скобках [] указан индекс - порядковый номер;

        }
    }
}